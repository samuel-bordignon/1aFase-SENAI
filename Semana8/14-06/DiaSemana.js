resposta = prompt('Digite um número de 1 a 7: ')
switch (resposta) {
    case '1':
        alert('O número digitado corresponde a Domingo, primeiro dia da semana')
        break
    case '2':
        alert('O número digitado corresponde a Segunda-feira, segundo dia da semana')
        break
    case '3':
        alert('O número digitado corresponde a Terça-feirai, terceiro dia da semana')
        break
    case '4':
        alert('O número digitado corresponde a Quarta-feira, primeiro dia da semana')
        break
    case '5':
        alert('O número digitado corresponde a Quinta-feira, quinto dia da semana')
        break
    case '6':
        alert('O número digitado corresponde a Sexta-feira, sexto dia da semana')
        break
    case '7':
        alert('O número digitado corresponde a Sábado, sétimo dia da semana')
        break
    default:
        alert('Número digitado inválido')
}
